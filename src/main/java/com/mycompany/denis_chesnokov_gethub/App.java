    private void replaceAfterMax(int[] arr) {
     if (arr == null || arr.length == 0) {
            return;
        }

        int maxIndex = 0;
        for (int i = 1; i < arr.length; i++) {
            if (arr[i] > arr[maxIndex]) {
                maxIndex = i;
            }
        }

       
        for (int i = maxIndex + 1; i < arr.length; i++) {
            arr[i] = -1;
        }
 
     }     
