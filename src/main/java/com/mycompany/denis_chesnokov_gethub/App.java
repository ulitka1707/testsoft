      public static int calculateSumOfColumnMinima(int[][] matrix) {
        int sum = 0;
        for (int j = 0; j < 5; j++) { 
            int min = matrix[0][j];
            for (int i = 1; i < 5; i++) { 
                if (matrix[i][j] < min) {
                    min = matrix[i][j];
              
                }
            }
            sum += min;
        }
        return sum;
    }
      
